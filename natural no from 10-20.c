#include<stdio.h>
int main()
{
    //declaring variable
    int i=0;
    //using while loop
    while (i<=20)
    {
        //using nested if loop
        if (i>=10)
        {
            //printing output
            printf("The value of i is %d \n",i);
        }
        //increment of i value
        i++;
    }
    return 0;
}
